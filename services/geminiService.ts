const ai = new GoogleGenAI({ apiKey: process.env.API_KEY });

export async function generateVeoPrompt(params: PromptGenerationParams): Promise<VeoPromptResponse> {
  const finalPrompt = buildGeminiPrompt(params);

  const config: any = {};
  if (params.useGoogleSearch) {
    config.tools = [{googleSearch: {}}];
  }
  
  try {
    const response = await ai.models.generateContent({
      model: params.model,
      contents: finalPrompt,
      config,
    });
    return {
      prompt: response.text.trim(),
      groundingChunks: response.candidates?.[0]?.groundingMetadata?.groundingChunks
    };
  } catch (error) {
    console.error("Error generating prompt with Gemini:", error);
    throw error;
  }
}

export async function generateConceptArt(prompt: string): Promise<string> {
    const imagePrompt = `Create a single, highly-detailed piece of concept art based on the following scene description. Focus on the mood and key elements. The scene: ${prompt}`;
    try {
        const response = await ai.models.generateImages({
            model: 'imagen-4.0-generate-001',
            prompt: imagePrompt,
            config: {
              numberOfImages: 1,
              outputMimeType: 'image/png',
              aspectRatio: '16:9',
            },
        });

        if (response.generatedImages && response.generatedImages.length > 0) {
            return response.generatedImages[0].image.imageBytes;
        } else {
            throw new Error("No image was generated by the API.");
        }

    } catch(error) {
        console.error("Error generating image with Gemini:", error);
        throw error;
    }
}

export async function editConceptArt(imageDataUrl: string, prompt: string): Promise<EditedImageResponse> {
    const match = imageDataUrl.match(/^data:(image\/\w+);base64,(.*)$/);
    if (!match) {
        throw new Error("Invalid image data URL format");
    }
    const mimeType = match[1];
    const imageData = match[2];

    try {
        const response = await ai.models.generateContent({
            model: 'gemini-2.5-flash-image',
            contents: {
                parts: [
                    {
                        inlineData: {
                            data: imageData,
                            mimeType: mimeType,
                        },
                    },
                    {
                        text: prompt,
                    },
                ],
            },
            config: {
                responseModalities: [Modality.IMAGE, Modality.TEXT],
            },
        });

        for (const part of response.candidates[0].content.parts) {
            if (part.inlineData && part.inlineData.mimeType.startsWith('image/')) {
                return { 
                    newMimeType: part.inlineData.mimeType,
                    newImageBytes: part.inlineData.data
                };
            }
        }
        
        throw new Error("The API did not return an image.");

    } catch (error) {
        console.error("Error editing image with Gemini:", error);
        throw error;
    }
}

